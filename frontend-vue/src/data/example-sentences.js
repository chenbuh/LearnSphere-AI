/**
 * 根据单词获取真实例句
 * @param {string} word - 单词
 * @param {string} meaning - 单词含义（用于生成备用例句）
 * @returns {Object} - 包含英文例句和中文翻译的对象
 */
export function getExampleSentence(word, meaning = '') {
  const lowerWord = word.toLowerCase().trim();
  
  // 首先查找真实例句库
  if (realExamples[lowerWord]) {
    return realExamples[lowerWord];
  }
  
  // 如果没有找到，生成一个通用的英文例句
  return {
    en: `I need to learn the word "${word}" today.`,
    cn: `我今天需要学习"${word}"这个单词。`
  };
}

/**
 * 检查单词是否有真实例句
 * @param {string} word - 单词
 * @returns {boolean}
 */
export function hasRealExample(word) {
  return !!realExamples[word.toLowerCase().trim()];
}


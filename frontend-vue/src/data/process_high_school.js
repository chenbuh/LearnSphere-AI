// 从文件中提取所有单词
function extractWordsFromFile(filePath) {
    const content = fs.readFileSync(filePath, 'utf-8');
    const wordPattern = /"word":\s*"([^"]*)"/g;
    const words = [];
    let match;
    while ((match = wordPattern.exec(content)) !== null) {
        words.push(match[1]);
    }
    return words;
}

// 生成高中词汇文件
function generateHighSchoolFile() {
    const inputFile = path.join(__dirname, 'high_school_words.js');
    const words = extractWordsFromFile(inputFile);

    // 为每个单词添加翻译和音标
    const wordData = words.map(word => {
        const lowerWord = word.toLowerCase();
        const vocab = highSchoolVocabDB[lowerWord];
        if (vocab) {
            return {
                word: word,
                translation: vocab.translation,
                phonetic: vocab.phonetic,
                level: "high_school"
            };
        } else {
            // 对于没有数据的单词，保留空字符串
            return {
                word: word,
                translation: "",
                phonetic: "",
                level: "high_school"
            };
        }
    });

    const content = `/**
 * 高中英语词汇数据
 * 包含高中英语核心词汇
 * 总词汇量: ${wordData.length} 个
 */

export const highSchoolWords = ${JSON.stringify(wordData, null, 4)};
`;

    const outputPath = path.join(__dirname, 'high_school_words.js');
    fs.writeFileSync(outputPath, content, 'utf8');

    const completedCount = wordData.filter(w => w.translation && w.phonetic).length;
    console.log(`✅ 高中词汇文件已生成: ${wordData.length} 个单词`);
    console.log(`   已补充完整数据: ${completedCount} 个单词`);
    console.log(`   待补充数据: ${wordData.length - completedCount} 个单词`);
}

function main() {
    console.log('开始生成高中词汇文件...\n');
    generateHighSchoolFile();
    console.log('\n完成！');
}

main();

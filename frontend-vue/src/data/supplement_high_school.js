// 从当前文件读取并更新
function updateHighSchoolFile() {
    const filePath = path.join(__dirname, 'high_school_words.js');
    const content = fs.readFileSync(filePath, 'utf-8');

    // 匹配整个数组
    const match = content.match(/export const highSchoolWords = (\[.*?\]);/s);
    if (!match) {
        console.log('无法找到词汇数组');
        return;
    }

    let words = JSON.parse(match[1]);
    let updatedCount = 0;

    words = words.map(item => {
        if (!item.translation || !item.phonetic) {
            const lowerWord = item.word.toLowerCase();
            if (extendedVocab[lowerWord]) {
                updatedCount++;
                return {
                    word: item.word,
                    translation: extendedVocab[lowerWord].translation,
                    phonetic: extendedVocab[lowerWord].phonetic,
                    level: "high_school"
                };
            }
        }
        return item;
    });

    // 写回文件
    const newContent = `/**
 * 高中英语词汇数据
 * 包含高中英语核心词汇
 * 总词汇量: ${words.length} 个
 */

export const highSchoolWords = ${JSON.stringify(words, null, 4)};
`;

    fs.writeFileSync(filePath, newContent, 'utf8');

    const completedCount = words.filter(w => w.translation && w.phonetic).length;
    console.log(`✅ 高中词汇文件已更新`);
    console.log(`   已补充完整数据: ${completedCount}/${words.length} 个单词`);
    console.log(`   本次新增: ${updatedCount} 个单词`);
}

function main() {
    console.log('开始更新高中词汇文件...\n');
    updateHighSchoolFile();
    console.log('\n完成！');
}

main();

def load_existing_words(file_path):
    """加载现有的词汇文件"""
    with open(file_path, 'r', encoding='utf-8') as f:
        content = f.read()
        # 提取JSON部分
        match = re.search(r'export\s+\w+\s*=\s*(\[.*?\]);', content, re.DOTALL)
        if match:
            json_str = match.group(1)
            return json.loads(json_str)
    return []

def save_words_with_translation(words, vocab_db, level, output_file):
    """保存包含翻译和音标的词汇"""
    for word_obj in words:
        word = word_obj['word'].lower().strip()
        if word in vocab_db:
            word_obj['translation'] = vocab_db[word]['translation']
            word_obj['phonetic'] = vocab_db[word]['phonetic']
        else:
            # 如果没有找到，保持原样或设置默认值
            word_obj['translation'] = ""
            word_obj['phonetic'] = ""

    # 生成JavaScript文件
    with open(output_file, 'w', encoding='utf-8') as f:
        if level == "primary":
            f.write("/**\n * 小学英语词汇数据\n * 包含小学英语大纲核心词汇\n * 总词汇量: {} 个\n */\n\n".format(len(words)))
            f.write("export const primarySchoolWords = ")
        else:
            f.write("/**\n * 高中英语词汇数据\n * 包含高中英语核心词汇\n * 总词汇量: {} 个\n */\n\n".format(len(words)))
            f.write("export const highSchoolWords = ")

        json.dump(words, f, ensure_ascii=False, indent=4)
        f.write(";\n")

    print(f"已生成 {output_file}，共 {len(words)} 个单词")

